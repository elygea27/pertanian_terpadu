			<h1>Tips Merawat Kaktus di Dalam Ruangan</h1>
			<img src="<?php echo base_url('asset/A7/kaktus.png') ?>">
	</div>
	
	<div class="content">
		<p>&nbsp Kaktus adalah salah satu tanaman hias yang banyak digemari karena bentuknya unik dan perawatannya yang mudah. Meskipun kaktus tidak membutuhkan banyak perlakuan, jika tidak dirawat dengan tepat akan menghambat pertumbuhannya. Masih banyak yang belum mengetahui bagaimana merawat kaktus agar berumur panjang dan tetap indah. Oleh karena itu, artikel ini akan memaparkan berbagai tips perawatan kaktus di rumah</p>

		<h2><br>Pengaturan Cahaya</h2>

		<p><br>&nbsp Kaktus memiliki habitat asli di daerah kering dengan intensitas pencahayaan yang tinggi selama 7-8 jam. Tanaman kaktus yang kurang mendapat cahaya akan tampak kecil dan berwarna hijau pucat. Oleh karena itu, apabila Anda memelihara kaktus di dalam ruangan maka pastikan kaktus terkena cahaya sinar matahari yang cukup, seperti dekat jendela.</p>

		<p>&nbsp Kaktus juga membutuhkan tempat yang teduh sesekali agar tidak mengalami fototoksisitas akibat terpapar cahaya berlebih. Cahaya matahari yang berlebih menyebabkan kaktus menjadi pucat kekuningan. Beberapa jenis kaktus yang menyukai tempat yang teduh, sehingga cukup dijemur tidak langsung di bawah sinar matahari, minimal seminggu sekali.</p>

		<h2><br>Penyiraman</h2>

		<p><br>&nbsp Meskipun kebutuhannya akan air relatif sedikit, siram kaktus secara rutin minimal seminggu sekali atau saat media tanamnya mengering. Kaktus dapat disiram menggunakan sprayer atau dikocor biasa hingga airnya keluar dari lubang-lubang pot. Penyiraman yang berlebih dapat mengakibatkan akar hingga batang membusuk dan tanaman mati. Penyiraman dapat dilakukan pada pagi atau sore hari.</p>

		<h2><br>Pemupukan</h2>

		<p><br>&nbsp Pemupukan pada kaktus dapat dilakukan minimal seminggu sekali. Pupuk yang digunakan dapat berupa pupuk kandang, kompos, NPK, maupun pupuk slow release. Pupuk kandang dan kompos dapat ditambahkan setelah mengurangi setengah tanah dalam pot kemudian pupuk dimasukkan. Apabila ingin menggunakan pupuk NPK atau pupuk khusus bunga, dapat dilarutkan dalam air sesuai dosis rekomendasi, kemudian disemprotkan pada kaktus dan medianya. Jangan melakukan pemupukan berlebih atau saat tanah basah agar pertumbuhan tanaman tidak terganggu. Waktu pemupukan baiknya dilakukan pada sore hari.</p>

		<h2><br>Repotting</h2>

		<p><br>&nbsp Tanaman kaktus yang sehat membutuhkan repotting yang sering luput untuk dilakukan para pemilik kaktus. Repotting bertujuan memperbarui unsur hara  dan memberi ruang lebih besar untuk pertumbuhan kaktus. Tahapan repotting ini dapat dilakukan dengan mengganti media tanam atau sekaligus wadah tanamnya. Media tanam kaktus memiliki karakter yang remah, tidak terlalu padat, dan basah. Anda dapat menggunakan media tanam yang sudah tersedia di toko-toko pertanian, atau membuat sendiri dengan mencampurkan tanah:kompos:pasir (1:1:2).</p>

		<p>&nbsp Saat membongkar kaktus dari pot lama, basahi media terlebih dahulu agar mencabut perakaran lebih mudah. Semprotkan fungisida pada perakaran untuk mencegah jamur, kemudian simpan di tempat yang kering dan bersih. Siapkan media tanam yang steril dan pot baru yang berukuran lebih besar dari pot yang lama. Bagian alas dalam pot diberi pecahan genting atau kerikil, kemudian tambahkan media tanam mencapai sepertiga tinggi pot. Selanjutnya, tanam kaktus dan penuhi media tanam dalam pot hingga dapat menopang tegak tanaman. Letakkan pot di tempat yang teduh dan jangan disiram selama 2 minggu pertama agar cepat beradaptasi. Lakukan repotting minimal 6 bulan sekali, terutama untuk kaktus yang memiliki pertumbuhan yang cepat.</p>

		<h2><br>Pengendalian Hama dan Penyakit</h2>

		<p><br>&nbsp Hama yang biasanya menyerang kaktus berupa tungau, kutu putih, dan laba-laba. Pengendalian hama cukup dilakukan dengan menyikat kaktus dengan kuas atau sikat halus yang bersih. Anda dapat menambahkan 1 sendok detergen/sabun colek ke dalam 5 liter air kemudian digosokkan menggunakan sikat atau kuas pada permukaan kaktus.</p>

		<p>Kaktus dapat terserang penyakit berupa busuk pangkal batang, penyakit tepung, maupun kapang jelaga. Pengendalian penyakit sebaiknya dilakukan secara preventif dengan menjaga sanitasi dan menggunakan bibit yang sehat. Apabila kaktus terserang penyakit, semprotkan tepung belerang pada bagian batang yang terinfeksi.</p>

