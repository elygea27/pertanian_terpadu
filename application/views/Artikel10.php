	<div class="content">
		<p>Salah satu unsur terpenting dan mendasar dalam proses pembangunan pertanian adalah pada unsur sumber daya manusianya, yaitu petani. Petani berperan dalam proses budidaya hingga produksi hasil pertanian. Petani adalah orang yang bergerak dalam usahatani, mulai dari pengolahan lahan, budidaya tanaman sampai dengan memanen hasil yang akan digunakan untuk kepentingan pribadi maupun dipasarkan. Petani harus menjadi unsur yang paling diperhatikan dalam pembangunan pertanian.</p>

		<p><br>Hingga saat ini muncul berbagai permasalahan yang dihadapi petani Indonesia. Jika dilihat lebih mendalam soal permasalahan petani, dapat dikatakan bahwa masalah utama petani adalah bagaimana cara mereka mendapatkan hasil panen yang maksimal. Jika petani tidak bisa mendapat hasil panen yang maksimal, maka tidak akan didapat pula penghasilan. Padahal penghasilan ini yang nantinya akan dibuat untuk mencukupi kebutuhan hidup maupun untuk memulai usahataninya kembali. Petani dalam memulai usahataninya pasti membutuhkan input berupa modal, baik berupa uang maupun sarana produksi pertanian. Apalagi jika petani mengalami kerugian dalam proses usahataninya, baik karena iklim ekstrem maupun hama dan penyakit tanaman.</p>

		<p><br>Permodalan adalah permasalahan paling mendasar yang sering dihadapi petani. Modal sering menjadi kendala seorang petani dalam melakukan usahataninya. Keterbatasan modal juga membuat kuantitas dan kualitas hasil yang didapat petani tidak maksimal. Permasalahan modal ini juga menjadi penyebab utama banyaknya petani yang hidup di bawah garis kemiskinan. Menurut data BPS tahun 2016, dari total 27,7 juta orang yang digolongkan miskin di Indonesia, 21,8% di antaranya berprofesi sebagai petani. Ada sekitar 6,05 juta petani yang hidup di bawah garis kemiskinan. Bahkan pada data pertambahan jumlah penduduk miskin Indonesia dari tahun 2015-2016, 60% angkanya disumbangkan dari profesi petani atau sekitar 516.000 jiwa.</p>

		<p><br>Sifat budidaya yang lebih tergantung dengan alam, kegagalan panen yang dialami petani tentu menjadi permasalahan yang sangat serius. Petani yang tergolong miskin seringkali tidak memiliki tabungan untuk menutupi kerugian usahataninya. Akan muncul masalah bagaimana petani mendapatkan modal untuk memulai kembali usahataninya, mulai dari pembelian bibit, pupuk, pestisida, dan sarana produksi pertanian lainnya. Petani akan dituntut untuk mendapat modal dari pihak lain.</p>

		<p><br>Terdapat beberapa lembaga yang sebenarnya bisa menyediakan modal bagi petani yang membutuhkan, namun sifatnya pinjaman. Pemerintah melalui bank milik negara sudah menyediakan fasilitas melalui kredit. Namun, menurut Saputra (2003) dalam penelitiannya di Balai Besar Pengkajian dan Pengembangan Teknologi Pertanian tentang aksesibilitas petani kecil pada sumber kredit, petani kecil atau miskin memiliki banyak kendala untuk mendapat akses modal ke lembaga formal seperti bank, di antaranya:</p>

		<p><br>1. Petani tidak memiliki agunan sertifikat tanah.</p>
		<p>2. Pembayaran secara bulanan tidak sesuai dengan usahatani yang memberikan siklus produksi musiman.</p>
		<p>3. Petani kecil umumnya belum familier dengan prosedur administrasi yang rumit.</p>

		<p><br>Berdasarkan data Survei Sosial Ekonomi Nasional (Susenas) tahun 2016, menyatakan bahwa hanya 15% petani yang mengakses kredit di bank. Terdapat permasalahan kredit melalui perbankan yang dirasakan petani, seperti prosedur yang birokratis, tinggi risiko, dan besarnya suku bunga. Sedangkan jika petani memilih untuk melakukan kredit ke lembaga non bank atau informal tentu akan lebih merugikan karena tingkat suku bunga yang sangat tinggi. Berbagai kondisi tersebut tentu sangat memberatkan petani dan bisa menjadi permasalahan baru untuk mendapatkan modal.</p>

		<p><br>Petani membutuhkan akses permodalan yang mudah dan cepat. Petani umumnya hanya memiliki penghasilan setelah masa panen. Jika lahan petani mengalami cuaca ekstrem maupun serangan hama dan penyakit sehingga gagal panen, harus ada langkah yang cepat agar petani mendapatkan modal kembali untuk memulai usahataninya maupun memulihkan kondisi lahannya. Modal yang dibutuhkan tidak selalu berjumlah besar, namun yang terpenting adalah harus didapatkan secara mudah dan cepat.</p>

