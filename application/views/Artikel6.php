		<p>&nbsp Jeruk merupakan salah satu tanaman yang paling banyak ditanam atau dibudidayakan di pekarangan. Jeruk menjadi tanaman hias buah favorit dibandingkan tanaman lainnya. Selain dapat dipanen buahnya, tanaman jeruk juga memiliki estetika tersendiri karena memiliki bentuk daun yang khas. Budidaya tanaman jeruk juga bisa dilakukan secara tabulampot agar lebih memiliki nilai estetika.</p>

		<p>&nbsp Terdapat berbagai jenis tanaman jeruk yang menjadi primadona banyak orang. Setiap jenis jeruk akan memiliki karakteristik dan kelebihan tersendiri. Berikut ini adalah berbagai jenis jeruk yang cocok ditanam di pekarangan rumah Anda.</p>

		<h2><br>Jeruk Nagami</h2>

		<p><br>&nbsp Jeruk nagami merupakan jenis jeruk yang termasuk dalam varietas unggul. Jeruk ini memiliki bentuk buah yang lonjong dan bisa dimakan langsung dengan kulitnya. Walaupun bentuk buahnya berbeda dengan jeruk lainnya, tetapi rasanya tidak kalah manis. Jenis jeruk nagami menjadi jenis jeruk yang paling direkomendasikan untuk dijadikan tanaman hias buah pekarangan Anda. Hal ini karena jenis jeruk tersebut paling rajin berbuah dengan tinggi tanaman tidak mencapai 1 meter.</p>

		<br><center><img src="<?php echo base_url('asset/A6/nagami.jpg') ?>"></center>

		<p><br>&nbsp Proses budidaya jeruk nagami dapat dikatakan mudah dan sederhana. Jeruk nagami bisa ditanam dengan teknik tabulampot dengan pemeliharaan yang tidak terlalu intensif. Dengan menanamnya dalam pot, tentu juga akan menambah nilai estetika tanaman ini. budidaya dapat dilakukan dengan memilih bibit yang tepat, pemupukan teratur, dan pemangkasan jika diperlukan.</p>

		<h2><br>Jeruk Sunkist</h2>

		<p><br>&nbsp Jeruk sunkist merupakan jenis jeruk yang dikenal karena rasanya yang manis dan memiliki aroma segar. Jeruk sunkist memiliki bentuk buah yang cenderung bulat dan berukuran lebih kecil dibanding jeruk pada umumnya. Jeruk sunkist sebenarnya lebih memiliki nilai estetika dibanding jeruk nagami, tetapi dalam proses budidaya harus lebih intensif dan perlu pemahaman yang lebih.</p>

		<br><center><img src="<?php echo base_url('asset/A6/sankis.jpg') ?>"></center>

		<p><br>&nbsp Budidaya jeruk sunkist bisa dilakukan dengan teknik tabulampot. Bibit bisa didapatkan dengan teknik okulasi atau cangkok agar pembuahan lebih produktif dan bisa juga dengan membeli dengan memperhatikan kualitasnya. Ciri bibit yang baik di antaranya berbatang utama lurus tegak, bercabang rindang, daunnya berwarna hijau segar, dan belum berbuah. Jeruk sunkist juga cocok dijadikan peluang usaha karena harga di pasaran yang relatif tinggi.</p>

		<h2><br>Jeruk Bali atau Pamelo</h2>

		<p><br>&nbsp Jeruk bali merupakan jenis jeruk yang dikenal dengan ukuran buah yang besar. Buah jeruk bali memang bisa dibilang berukuran lebih besar dibanding jenis jeruk lainnya, memiliki campuran rasa manis dan asam, serta memiliki bulir jeruk yang berukuran besar. Karakteristik lain yang menjadi khas dari jeruk jenis ini adalah warna daging buah dan bulirnya yang berwarna merah. Dengan bentuk buah yang unik ini tentu akan membuat pekarangan Anda semakin memiliki nilai estetika.</p>

		<br><center><img src="<?php echo base_url('asset/A6/bali.png') ?>"></center>

		<p><br>&nbsp Budidaya jeruk bali tidak jauh berbeda dengan jeruk lainnya, jeruk bali juga bisa ditanam dengan teknik tabulampot. Bibit dapat ditumbuhkan dari biji buah, tetapi akan lebih baik jika Anda membeli langsung dalam bentuk bibit siap tanam hasil stek atau cangkok agar lebih cepat berbuah. Perlu diperhatikan ukuran pot untuk menanam harus disesuaikan dengan ukuran tanaman, mengingat tanaman jeruk bali ini bisa tumbuh besar</p>

