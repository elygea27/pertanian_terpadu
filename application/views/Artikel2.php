	<div class="content">
		<p>&nbsp Padi merupakan tanaman yang banyak dibudidayakan oleh para petani. Produksi padi dituntut selalu tinggi untuk mencukupi kebutuhan pangan nasional. Perlu adanya upaya khusus untuk meningkatkan produksi padi agar mampu memenuhi kebutuhan tersebut. Banyak upaya yang bisa dilakukan, diantaranya adalah pada pengaturan jarak tanam dan pengairan tanaman padi di sawah.</p>

		<p>&nbsp Budidaya padi varietas unggul tidak akan optimal jika tidak menggunakan teknik budidaya yang tepat, terutama pada pengaturan jarak tanamnya. Perbedaan jarak seringkali tidak mendapat perhatian dalam menentukan produktivitas padi. Jarak tanam seringkali bervariasi antarpetani dan antarlokasinya, sehingga akan menyebabkan kesalahan perhitungan data produksi padi nasional. Oleh karena itu perlu adanya standarisasi pengukuran populasi tanaman per satuan luas dan konversi gabah hasil dari ubinan ke hektar berdasarkan jarak tanam padi di lapangan.</p>
		
		<h2><br>Jarak Tanam</h2>
		
		<p><br> &nbsp Pengaturan jarak tanam menjadi penting bagi tanaman karena dapat mempengaruhi penangkapan cahaya matahari untuk fotosintesis, efektivitas penyerapan hara oleh akar, kebutuhan air, sirkulasi udara, iklim mikro (suhu dan kelembaban), serta populasi gulma dan OPT. Jarak tanam yang sering digunakan petani mulai dari 20 cm x 20 cm; 25 cm x 25 cm; 27,5 cm x 27,5 cm; dan 30 cm x 30 cm hingga sistem jajar legowo.</p>

		<p> &nbsp Pengaplikasian sistem jajar legowo menjadi lebih efektif untuk meningkatkan populasi tanaman dan mengurangi serangan OPT, seperti tikus dan keong mas. Jajar legowo merupakan sistem pengaturan jarak tanam dengan pengosongan satu baris tanaman setiap dua atau lebih baris dan merapatkan dalam barisan tanaman, sehingga dikenal legowo 2:1 jika satu baris kosong diselingi oleh dua baris padi atau 4:1 jika diselingi empat baris tanaman.</p>

		<h2><br>Pengairan</h2>

		<p><br> &nbsp Pengaturan pengairan atau irigasi juga menjadi penting untuk optimalisasi budidaya padi. Penggenangan sawah terus-menerus dinilai sangat boros dalam budidaya padi. Harus dilakukan penghematan, terutama pada musim kemarau. Strategi yang dapat digunakan adalah dengan menerapkan pemilihan varietas dan metode pengelolaan air seperti macak-macak, berselang/intermittent, dan alternasi pengairan kering (PBK). Dengan cara-cara ini areal sawah dapat dialiri pada musim kemarau menjadi 2 kali lebih luas.
		</p>

		<p> &nbsp Yang perlu diperhatikan dalam penghematan air atau irigasi adalah mengurangi aliran yang tidak produktif seperti rembesan, perkolasi, dan evaporasi, serta memelihara aliran transpirasi. Hal ini bisa diupayakan mulai saat persiapan lahan, penanaman, dan selama pertumbuhan tanaman. Selain itu untuk menghemat air dapat dengan memilih varietas padi yang relatif toleran terhadap kekurangan air dan bisa ditanam di lahan sawah kering seperti Inpari 10 laeya, Inpari 11, Inpari 12, Inpari13, Inpari 19, Inpari 38-41 Tadah Hujan Agritan.
		</p>

		<p> &nbsp Penggunaan dan pemanfaatan air pada satu wilayah cenderung harus berbeda dengan wilayah lain karena melihat perbedaan karakteristik distribusi curah hujan, kondisi infrastruktur jaringan irigasi, tingkat kerawanan kekeringan, parameter fisika tanah, hidrologi lahan, teknik budidaya, cara pengairan dari petak ke petak, dan organisasi pemakai air.
		</p>
		
		<p> &nbsp Pengaturan air atau irigasi dapat meningkatkan intensitas tanam, mengurangi kebutuhan debit air 15 harian, dan mengurangi dampak kekeringan melalui pengairan basah kering dan pengairan berselang.
		</p>

