			<h1>Tips Budidaya Kelapa Kopyor untuk Mendapat Hasil Maksimal</h1>
			<img src="<?php echo base_url('asset/A1/kelapa1.jpeg') ?>">
	</div>
	
	<div class="content">
		<p>&nbsp Sudah kita ketahui bersama jika kelapa kopyor merupakan salah satu jenis kelapa yang bernilai ekonomi tinggi. Kelapa ini dicirikan dengan daging buah yang bertekstur gembur serta rasa yang gurih. Selama ini kelapa kopyor merupakan buah langka karena hanya dihasilkan dari pohon yang memiliki gen atau pembawa sifat kopyor saja. Oleh karena itu tidak salah jika banyak orang yang ingin mengusahakan budidaya jenis kelapa kopyor.</p>

		<p>&nbsp Budidaya kelapa kopyor bisa dilakukan siapa saja. Pohon kelapa ini bisa dijadikan perindang di halaman rumah, selain akan mendapat hasil buahnya, lingkungan akan lebih teduh dan asri. Namun tidak menutup kemungkinan juga budidaya kelapa kopyor ini bisa dijadikan usaha karena nilai ekonominya yang tinggi. Ada beberapa tips yang perlu diketahui dalam membudidayakan kelapa kopyor agar hasilnya lebih maksimal.</p>
		
		<h2><br>Perbanyakan</h2>
		
		<p><br> &nbsp Biasanya perbanyakan dilakukan secara generatif, yaitu kelapa kopyor dapat diperoleh melalui cara penanaman kembali buah kelapa normal yang dijadikan sebagai bibit dari pohon kelapa yang berbuah kopyor. Namun cara ini kurang efektif karena jenis buah kelapa kopyor dalam satu pohon yang dihasilkan sangat sedikit.</p>

		<p> &nbsp Cara modern yang telah dilakukan adalah perbanyakan secara in vitro. Cara ini dengan menumbuhkan embrio dari buah kopyor pada media tumbuh buatan dalam kondisi aseptik di laboratorium. Tanaman yang dihasilkan dengan cara ini akan menghasilkan 90% hingga 100% buah kopyor. Sebenarnya cara ini sudah dilakukan oleh Balai Penelitian Bioteknologi Perkebunan Indonesia (BPBPI) dan hasil bibitnya juga sudah dipasarkan sehingga dapat dimanfaatkan oleh para pembudidaya. Tanaman kelapa kopyor yang di perbanyak dengan cara in vitro sduah banyak di tanam di Ciomas (Bogor), Riau, dan Kalimantan Timur.</p>

		<h2><br>Penanaman</h2>

		<p><br> &nbsp Jika Anda menanam kelapa dari bibit perlu diperhatikan proses penanamannya, terutama saat pemindahan bibit ke lahan atau kebun. Pada kasus pemindahan bibit kelapa kopyor yang terlalu muda atau kurang dari 8 bulan akan memiliki resiko kematian bibit. Sedangkan pemindahan bibit kelapa kopyor yang terlalu tua atau lebih dari 1 tahun akan berisiko mengalami stagnasi (berhenti tumbuh sementara) sehingga menghambat pertumbuhan. Maka dari itu bibit kelapa kopyor yang ideal untuk ditanam adalah  yang berumur delapan bulan sampai satu tahun.
		</p>

		<p> &nbsp Ada baiknya juga, bibit kelapa kopyor ditanam di areal yang tidak berdekatan dengan kelapa biasa, minimal jarak 500 meter. Untuk mencapai produksi maksimal disarankan pula ditanam pada lahan dengan ketinggian di bawah 200 meter dan jarak tanam yang dipakai  9 meter x 9 meter, atau 10 meter x 10 meter dengan pola segitiga. Populasi pohon antara 100-140 pohon per hektar.
		</p>

		<h2><br>Waktu Penanaman yang Tepat</h2>

		<p><br> &nbsp Waktu yang bisa dibilang tepat untuk mulai membudidayakan kelapa kopyor adalah saat perpindahan musim penghujan ke musim kemarau. Hal ini karena ketika musim penghujan, kadar air di dalam tanah sangat cukup dan dapat mengangkut semua unsur hara yang ada di dalam tanah ke tanaman. Ketika musim beranjak ke musim kemarau, tingkat cahaya matahari akan meningkat. Waktu inilah yang paling disukai oleh pohon untuk mengembangkan tunasnya. Jadi menanam bibit kelapa kopyor sebaiknya berada di akhir musim penghujan dan mendekati musim kemarau.			
		</p>

		<h2><br>Pemeliharaan</h2>

		<p><br> &nbsp Pemeliharaan penting dilakukan saat setelah penanaman bibit. Pemeliharaan dilakukan dengan memberi pupu pada lubang tanam dengan kompos atau pupuk kandang. Pupuk ini akan menunjang pertumbuhan bibit karena akan menyediakan nutrisi dan zat-zat tumbuh. Selain itu usahakan bibit mendapat intensitas cahaya matahari sekitar 50%. Selanjutnya adalah menjaga agar lingkungan tidak kering dengan menyiramnya. Namun jika pohon sudah tumbuh dan daun sudah mekar maka tidak perlu perawatan intensif lagi.			
		</p>
		
		<p> &nbsp Salah satu tantangan dalam budidaya kelapa kopyor adalah hama kumbang, atau kebanyakan orang Jawa menyebutnya “kuwawung”. Hama menyerang dengan menggerogoti pohon hingga buah sehingga sangat mengganggu produktivitas. Untuk mengendalikan hama ini atau lainnya dapat menggunakan insektisida secukupnya
		</p>

