	<div class="content">
		<p>&nbsp Ketahanan pangan merupakan kondisi tersedianya bahan pangan, baik dari sisi jumlah, kualitas, maupun aksesnya untuk masyarakat di wilayah tertentu. Isu ketahanan pangan semakin menguat bersamaan dengan semakin besarnya populasi manusia. Salah satu dampak dari tidak terpenuhinya ketahanan pangan adalah terjadi kerawanan pangan. Kerawanan pangan dapat terjadi di lingkup individu, keluarga, maupun wilayah tertentu.</p>

		<p>&nbsp Terdapat berbagai cara untuk mencapai tingkat ketahanan pangan, mulai dari meningkatkan produktivitas tanaman penghasil bahan pangan dan memanfaatkan sumber daya yang ada secara optimal. Cara yang dapat dilakukan hampir semua orang untuk mendukung ketahanan pangan adalah dengan memanfaatkan sumber daya yang ada di sekitarnya, terutama lahan pekarangan. Pemanfaatan lahan pekarangan memiliki potensi untuk penyediaan bahan pangan bagi keluarga.</p>
		
		<h2><br>Potensi Pekarangan</h2>
		
		<p><br> &nbsp Pekarangan merupakan sebidang tanah yang berada di sekitar rumah. Dari data Kementerian Pertanian, Indonesia memiliki luas pekarangan sekitar 10,4 juta hektar. Pekarangan ini dapat dimanfaatkan sebagai lahan untuk menghasilkan bahan pangan yang berdampak pada berkurangnya pengeluaran masyarakat. Selain itu, pekarangan juga bisa sebagai penghasil tanaman perdagangan, penghasil tanaman rempah-rempah atau obat-obatan, dan juga sumber berbagai macam kayu-kayuan.</p>

		<p> &nbsp Pekarangan sering disebut sebagai lumbung hidup, warung hidup atau apotek hidup. Dari buku Sajogyo tahun 1994 berjudul Menuju Gizi Baik yang Merata di Pedesaan dan Perkotaan, pekarangan disebut lumbung hidup karena sewaktu-waktu kebutuhan pangan pokok seperti beras, jagung, dan umbi-umbian tersedia di pekarangan. Selanjutnya, disebut sebagai warung hidup karena dalam pekarangan terdapat sayuran yang berguna untuk memenuhi kebutuhan konsumsi keluarga, di mana sebagian rumah tangga harus membelinya. Sementara itu, disebut sebagai apotek hidup karena di pekarangan dapat ditanami berbagai jenis tanaman obat yang sangat bermanfaat dalam penyembuhan penyakit secara tradisional.</p>

		<h2><br>Pemanfaatan Pekarangan</h2>

		<p><br> &nbsp Pemanfaatan pekarangan untuk mendukung ketahanan pangan dapat dilakukan semua orang, terutama masyarakat perkotaan. Mayoritas populasi manusia berada di perkotaan, bahkan dari data Badan Ketahanan Pangan, pada tahun 2050 masyarakat perkotaan mencapai 2/3 dari total penduduk. Pemanfaatan pangan sangat disarankan oleh masyarakat perkotaan dilatarbelakangi semakin menurunnya ketersediaan lahan produktif akibat alih fungsi lahan menjadi perumahan dan industri.
		</p>

		<p> &nbsp Data Badan Ketahanan Pangan juga menunjukkan terdapat 3,4 juta hektar lahan di perkotaan yang belum dimanfaatkan, termasuk pekarangan. Padahal 71% rumah tangga di perkotaan memiliki status kerawanan pangan dalam kategori sangat rawan. Selain itu, 40-60% pendapatan masyarakat perkotaan diperuntukkan membeli bahan pangan. Dengan memanfaatkan potensi lahan pekarangan yang ada, tentu akan sangat terasa dampak positifnya, terutama untuk mendukung ketahanan pangan.
		</p>

		<p><br> &nbsp Selama ini memang tidak ada catatan statistik yang secara spesifik mencatat berapa besar kontribusi pekarangan terhadap produksi pangan nasional. Data yang ada sifatnya adalah umum yaitu berupa jumlah produksi pertanian dari lahan kering, yang di dalamnya termasuk lahan pekarangan, terutama produksi tanaman tahunan buah-buahan. Hal ini terjadi karena pekarangan terlalu kompleks untuk dikuantitatifkan karena kontribusi pekarangan bersifat multi-dimensional. Namun, hasil kajian empiris mengungkapkan bahwa usaha di pekarangan jika dikelola secara intensif sesuai dengan potensi pekarangan, disamping dapat memenuhi kebutuhan konsumsi rumah tangga, juga dapat memberikan sumbangan pendapatan bagi keluarga.
		</p>
		
		<p><br> &nbsp Demi mendukung ketahanan pangan nasional melalui pemanfaatan pekarangan, pemerintah sendiri melalui Kementerian Pertanian sudah melakukan berbagai upaya, salah satunya adalah program KRPL (Kawasan Rumah Pangan Lestari). Dengan adanya program KPRL diharapkan pekarangan dapat dimanfaatkan dan mampu meningkatkan produktivitasnya. Sejak 2010 sampai sudah ada 18.000 titik lokasi KRPL dan akan ditingkatkan menjadi 23.000 titik pada tahun 2018 ini.			
		</p>

