			<h1>Berbagai Permasalahan Petani Indonesia yang Penting Diperhatikan</h1>
			<img src="<?php echo base_url('asset/A11/petani.jpg') ?>">
	</div>
	
	<div class="content">
		<p>Petani adalah orang yang melakukan budidaya tanaman, mulai dari penyiapan lahan, penanaman, pemeliharaan, sampai dengan memanen hasilnya. Hasil panen bisa digunakan untuk keperluan pribadi maupun dipasarkan. Petani merupakan pelaku utama dalam pembangunan pertanian Indonesia. Petani memiliki peran penting untuk menghasilkan produk pertanian, baik untuk keperluan bahan pangan maupun industri.</p>

		<p><br>Petani sebagai pelaku utama pembangunan pertanian tentu memiliki berbagai masalah. Masalah ini sering menghambat petani untuk mengembangkan usahataninya. Masalah yang dihadapi petani ini juga merupakan masalah dasar yang harus diselesaikan untuk melakukan pembangunan pertanian. Berikut adalah berbagai masalah petani Indonesia yang perlu diperhatikan.</p>

		<h2><br>Permodalan Terbatas</h2>

		<p><br>Permodalan sering menjadi masalah ketika petani akan memulai usahataninya. Modal menjadi masalah karena petani tidak selalu sukses dalam proses budidaya. Sifat hasil pertanian yang lebih tergantung ke alam membuat hasil panen tidak menentu. Sering terdapat kendala seperti cuaca ekstrim dan serangan OPT. Jika petani mengalami gagal panen atau merugi tentu petani jadi tidak memiliki modal untuk melakukan usahanya kembali.</p>

		<p><br>Sebenarnya penyedia modal bagi petani sudah ada, namun aksesnya masih terbatas. Lembaga penyedia modal biasanya juga jadi memberatkan petani karena prosedurnya yang rumit dan tingginya suku bunga. Berdasarkan hasil survei Susenas tahun 2016, hanya 15% petani Indonesia yang mengakses kredit untuk modal di bank. Petani lebih memilih untuk kredit di lembaga nonbank karena prosedurnya yang gampang walaupun dengan suku bunga yang lebih tinggi. Tentu ini sangat merugikan petani.</p>

		<h2><br>Harga Produk Tidak Stabil</h2>

		<p><br>Sifat hasil pertanian adalah musiman, yang berarti produk tersebut akan memiliki jumlah yang mencukupi pada waktu tertentu saja. Misalnya, pada musim panen padi akan melimpahnya pasokan beras, namun pada musim paceklik pasokannya sangat terbatas. Sifat ini tentu sangat mempengaruhi harga produk pertanian yang dihasilkan petani. Saat pasokan melimpah harga akan jatuh, sedangkan saat pasokan terbatas akan terjadi lonjakan harga.</p>

		<p><br>Petani harus cerdas dalam memasarkan hasil panennya. Jangan sampai seluruh hasil panen dijual saat harga sedang turun karena mampu merugikan petani. Salah satu strategi yang dapat dilakukan petani adalah penggudangan. Dengan langkah ini tentu produk pertanian yang ada di pasar dapat dikendalikan dan harga juga akan terkendali.</p>

		<h2><br>Penguasaan Lahan Sempit</h2>

		<p><br>Salah satu masalah yang terus menghantui pembangunan petani Indonesia adalah sempitnya penguasaan lahan. Dengan lahan garapan sempit tentu petani juga akan mendapat penghasilan terbatas dan membuat tingkat kesejahteraannya di bawah. Rata-rata petani Indonesia hanya memiliki luas lahan 0,2 hektar. Belum lagi masalah kondisi tanah yang kualitas tidak optimal karena penggunaan pupuk kimia dan pestisida.</p>

		<p><br>Penguasaan lahan yang sempit menyebabkan rendahnya kesejahteraan petani Indonesia. Petani dengan penghasilan yang kecil dan terbatas tentu akan sulit untuk mengembangkan skala usahataninya. Masalah ini masih terus mengakar dan menjadi pekerjaan rumah seluruh stakeholder terkait untuk membangun pertanian Indonesia lebih maju.</p>

		<h2><br>Kelangkaan Saprodi</h2>

		<p><br>Masalah terakhir yang sering dihadapi petani adalah kelangkaan saprodi atau sarana produksi pertanian. Padahal saprodi disini berperan sebagai input usahatani yang dilakukan petani. Sulitnya mendapatkan input ini tentu menjadi masalah karena mampu menghambat kelancaran usahatani. Disamping sulit, terkadang saprodi ini memiliki harga yang tinggi dan meresahkan petani.</p>

		<p><br>Salah satu penyebab kelangkaan saprodi ini adalah banyaknya kebocoran sistem distribusinya sehingga menyulitkan petani untuk mendapatkan input dengan mudah dan harga terjangkau. Perlu adanya regulasi yang lebih baik agar saprodi dapat terdistribusikan dengan baik dan tepat sasaran. Pengadaan Kartu Tani saat ini semoga mampu menanggulangi masalah distribusi saprodi ini.</p>

