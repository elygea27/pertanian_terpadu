			<h1>Mengetahui Sifat-Sifat Hasil Pertanian dan Cara Penanganannya</h1>
			<img src="<?php echo base_url('asset/A15/sayur.jpg') ?>">
	</div>
	
	<div class="content">
		<p>Sifat-sifat hasil pertanian penting diketahui, terutama untuk keperluan pemasaran. Hasil atau produk pertanian memiliki sifat berbeda dengan produk non pertanian. Secara kualitas tiap unit produknya sudah dapat dilihat perbedaannya, jika produk industri dapat menghasilkan produk yang seragam, namun jika pertanian akan menghasilkan produk yang beragam. Misalnya, dalam satu pohon jeruk tidak akan menghasilkan buah-buah jeruk yang memiliki kualitas yang sama dan identik.</p>

		<p><br>Mengetahui sifat hasil pertanian perlu diperhatikan juga untuk menentukan bagaimana langkah pemasarannya. Pemasaran yang dimaksud adalah bagaimana proses aliran hasil pertanian yang terjadi dari petani sampai ke tangan konsumen. Karena hasil pertanian yang memiliki sifat yang khas ini, maka diperlukan penanganan yang khusus. Berikut sifat-sifat hasil pertanian yang perlu diperhatikan dan bagaimana cara penanganannya.</p>

		<h2><br>Mudah Rusak</h2>

		<p><br>Hasil pertanian memang tidak mampu bertahan dalam waktu yang lama tanpa penanganan tertentu. Hal ini memang sudah menjadi menjadi karakteristik biologis dan fisiologis hasil pertanian. Namun, sifat ini juga sering disebabkan oleh rendahnya kualitas penangan pascapanen, seperti kandungan air yang terlalu tinggi sehingga produk mudah membusuk. Padahal, yang dibutuhkan konsumen adalah produk yang segar atau fresh.</p>

		<p><br>Cara penanganannya adalah bisa dengan penyimpanan, pengolahan, dan distribusi. Penyimpanan yang dimaksud adalah dengan menempatkan produk di tempat atau alat yang mampu menambah ketahanannya, seperti mesin pendingin atau gudang yang sudah dimodifikasi. Selanjutnya, pengolahan juga bisa dilakukan menjadi produk yang lebih awet, seperti pisang yang diolah menjadi keripik. Terakhir adalah dengan mendistribusikan produk secara cepat sebelum mengalami perusakan.</p>

		<h2><br>Makan Tempat (Rowa)</h2>

		<p><br>Hasil pertanian mayoritas bersifat rowa atau membutuhkan tempat atau ruang penyimpanan yang luas. Akibat sifat ini akan meningkatkan biaya pengangkutan dan penyimpanan produk. Padahal, harga produk tersebut biasa sangat kecil jika dibandingkan dengan volumenya. Hal ini akan menyebabkan biaya total pemasaran seringkali lebih besar dibandingkan dengan biaya produksinya.</p>

		<p><br>Cara penanganannya bisa dengan pengolahan. Pengolahan disini dimaksudkan agar produk tidak lagi memiliki volume ruang yang besar, atau jika walaupun volumenya tetap besar namun akan memiliki nilai jual yang lebih tinggi. Dengan pengolahan ini maka produk dapat dengan mudah disimpan dan menjadi praktis.</p>

		<h2><br>Musiman</h2>

		<p><br>Sifat yang unik dari hasil pertanian adalah musiman. Musiman berarti tidak setiap saat produk tersebut dihasilkan atau hasil produksinya akan diperoleh pada waktu-waktu tertentu. Sifat ini menyebabkan ketidakstabilan harga produk tertentu di pasaran, kadang akan berharga tinggi dan kadang juga sangat rendah. Selain itu, hasil pertanian biasanya juga dibutuhkan setiap saat. Oleh sebab itu perlunya penanganan khusus.</p>

		<p><br>Cara penangannya adalah dengan penyimpanan dan pengolahan. Penyimpanan yang dimaksud adalah dengan tidak menjualnya semua hasil pertanian saat musim panen, sebagian hasil bisa disimpan terlebih dahulu. Hal ini untuk mencegah turunnya harga produk karena jumlahnya yang terlalu banyak di pasar. Pengolahan juga bisa dilakukan agar produk menjadi berbeda, tahan lama, dan memiliki nilai jual yang lebih tinggi.</p>

		<h2><br>Kualitas Beragam</h2>

		<p><br>Hasil pertanian bisa dibilang selalu tergantung dengan alam. Manusia tidak bisa dengan pasti menjadikan produk yang dihasilkan akan seragam, oleh karenanya hasil pertanian memiliki sifat kualitas beragam. Pada satu musim panen pasti akan ada perbedaan tiap unitnya, misalkan tidak mungkin ada satu pohon jambu yang mampu menghasilkan jambu-jambu yang sama identik, pasti ukurannya akan berbeda-beda.</p>

		<p><br>Cara penanganannya bisa dengan melakukan sortasi dan standarisasi. Cara ini dilakukan dengan mengumpulkan produk yang seragam kualitasnya. Indikator yang dipakai bisa ukuran, berat, bentuk, warna, aroma, kesegaran, ataupun panjang produk. Jadi, dalam proses pemasaran akan lebih mudah dalam penentuan harganya.</p>

		<h2><br>Letaknya di Pedesaan</h2>

		<p><br>Sifat terakhir dari hasil pertanian adalah letaknya di pedesaan. Mayoritas semua hasil pertanian memang ditanam dan diproduksi di wilayah pedesaan. Padahal mayoritas konsumennya ada di daerah perkotaan.</p>

		<p><br>Cara penangannya adalah dengan distribusi, pengangkutan, dan pengolahan. Hasil pertanian harus didistribusikan ke luar pedesaan, yaitu dengan cara pengangkutan yang aman. Jangan sampai saat proses pengangkutan justru merusak produk yang dibawa. Untuk mengurangi risiko tersebut juga bisa dilakukan pengolahan terlebih dahulu.</p>

