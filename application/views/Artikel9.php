	<div class="content">
		<p>Budidaya tanaman gantung dapat dikatakan sebagai cara budidaya tanaman yang dilakukan dengan cara menggantung tanaman beserta media tanam dan wadahnya. Cara ini bisa diterapkan di dalam maupun di luar ruangan. Dengan cara ini kegiatan budidaya menjadi tidak membutuhkan lahan lagi, hanya membutuhkan tempat gantung dan wadah. Dengan adanya tanaman gantung di lingkungan rumah juga akan membuat kesan yang lebih indah dan asri.</p>

		<p><br>Budidaya tanaman gantung ini memiliki beberapa kelebihan lain, seperti akan memudahkan dalam perawatannya karena lebih mudah untuk dipindah. Pada umumnya tanaman dan wadah yang digunakan memang berukuran kecil dan sebagai tanaman hias. Namun dalam budidaya tanaman gantung ini juga harus diperhatikan beberapa aspek agar lebih terlihat indah dan tanaman juga mampu tumbuh dengan optimal.</p>

		<h2><br>Pemilihan Jenis Tanaman</h2>

		<p><br>Pemilihan jenis tanaman sebenarnya bisa menyesuaikan dengan keinginan. Namun akan lebih cocok jika memilih tanaman yang memiliki ukuran kecil atau panjang yang menjuntai seperti pakis dan kaktus, berdaun rimbun yang mampu menutupi sekelilingnya atau merambat seperti germanium, serta bunga yang menarik seperti anggrek. Yang perlu diperhatikan adalah jangan memilih tanaman dengan akar ekstensif seperti jenis semak dan pohon karena dapat merusak wadah.</p>

		<h2><br>Pemilihan Wadah</h2>

		<p><br>Pada pemilihan jenis wadah atau pot ini juga tergantung selera masing-masing dan jenis tanamannya. Hal ini karena akan berpengaruh secara fisik terhadap keindahannya. Ada beberapa jenis pot yang dapat digunakan, seperti pot plastik, pot tanah liat, atau bahkan pot anyaman tambang. Pot plastik memiliki kelebihan pada bobot yang paling ringan dan praktis. Jika pot tanah liat memiliki kelebihan mampu menjaga kelembaban media tanam, namun memiliki bobot yang berat serta mudah pecah dan berlumut, sehingga harus rajin dibersihkan agar tetap terlihat bersih dan menarik. Jenis-jenis pot ini juga memiliki bentuk yang beragam, pilih yang sesuai selera dan bentuk tanaman yang akan ditanam.</p>

		<h2><br>Pemilihan Tali Gantung</h2>

		<p><br>Pemilihan tali gantung perlu diperhatikan dalam hal kekuatan. Dalam budidaya tanaman gantung akan memerlukan tali yang kuat untuk menahan berat tanaman sampai dengan potnya. Jika menggunakan tali biasa lama-kelamaan akan tidak kuat lagi untuk menahan beban tersebut. Namun juga perlu diperhatikan kenampakan tali tersebut. Pilihlah tali yang akan tetap menjaga keindahan tanaman. Tali khusus seperti ini bisa didapatkan di toko-toko bunga, namun juga bisa dibuat dengan kreasi sendiri.</p>

		<h2><br>Peletakan Tanaman Gantung</h2>

		<p><br>Tanaman gantung bisa dipasang di luar maupun dalam ruangan. Namun perlakuan ini juga tergantung jenis tanaman yang ditanam. Jika tanaman memerlukan cahaya langsung seperti kaktus harus diletakkan di luar ruangan, begitu juga sebaliknya.  Selain itu,  paku atau penahan lain yang digunakan untuk menyangga harus dipastikan kuat sehingga tidak mudah lepas. Hindari peletakan tanaman gantung di tempat yang sering dilewati untuk mencegah hal yang tidak diinginkan. Tanaman gantung juga bisa diletakkan di atas teras rumah.</p>

		<h2><br>Perawatan Tanaman</h2>

		<p><br>Perawatan yang perlu dilakukan adalah penyiraman dan pemupukan. Penyiraman bisa dilakukan pagi dan sore hari dengan air secukupnya. Jika air yang disiramkan sering menetes dan mengotori lantai bisa disiasati dengan memilih wadah atau pot yang memiliki penadah pada bagian bawahnya. Tanaman gantung juga membutuhkan pemupukan untuk mendukung pertumbuhan tanamannya. Pastikan tanaman tumbuh dengan baik dan lakukan pemangkasan bila perlu seperti pada daun atau ranting yang kering dan tidak beraturan.</p>

